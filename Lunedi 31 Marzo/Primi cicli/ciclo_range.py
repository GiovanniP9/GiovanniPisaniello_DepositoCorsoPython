#range

# La funzione range() genera una sequenza di numeri interi.
# Pu√≤ essere utilizzata per iterare su una sequenza di numeri in un ciclo for.
for i in range(5):
    print(i)
print("Fine ciclo for")

#range con inizio e fine
for i in range(2, 10):
    print(i)
print("Fine ciclo for")

#range con inizio, fine e passo
# Il passo indica di quanto incrementare il valore ad ogni iterazione.
for i in range(1, 10, 2):
    print(i)
print("Fine ciclo for")

